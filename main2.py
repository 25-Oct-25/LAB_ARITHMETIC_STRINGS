sentence = "My name is Sana and I love learning Python programming daily"

word = "Sana"

print("Length of sentence:", len(sentence))

print("Index of first occurrence of the word:", sentence.find(word))

print("Number of times the word appears:", sentence.count(word))

print("Uppercase sentence:", sentence.upper())

print("Lowercase sentence:", sentence.lower())

print("Replace world:", sentence.replace(word,"Sana Alboshi"))

print("Last character of sentence:", sentence[-1])
